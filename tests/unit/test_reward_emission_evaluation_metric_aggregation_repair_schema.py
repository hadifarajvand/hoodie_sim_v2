class EvaluationInstrumentationRewardStateDiagnosticSchemaTests(unittest.TestCase):
    def test_checkpoint_metric_round_trip_schema(self) -> None:
        metric = CheckpointMetric(**_base_checkpoint_metric(budget=100))
        payload = metric.to_dict()
        self.assertEqual(payload["training_budget"], 100)
        self.assertEqual(payload["evaluation_decision_count"], 100)
        self.assertTrue(payload["replay_window_interpretation_warning"])

    def test_figure_manifest_requires_all_figures_when_generated(self) -> None:
        manifest = FigureManifest(
            figure_directory="artifacts/analysis/reward-emission-evaluation-metric-aggregation-repair/figures",
            figure_files=[
                "figure_01_raw_vs_canonical_reward_reconciliation.png",
                "figure_02_reward_event_coverage_by_budget.png",
                "figure_03_terminal_event_coverage_by_budget.png",
                "figure_04_completion_drop_pending_ratios_by_budget.png",
                "figure_05_policy_effect_after_repair.png",
            ],
            figure_count=5,
            figures_generated=True,
        )
        self.assertEqual(manifest.figure_count, 5)

    def test_report_round_trip_schema(self) -> None:
        report = RewardEmissionAggregationRepairReport(**_base_report_kwargs())
        payload = report.to_dict()
        self.assertEqual(payload["checkpoint_budgets"], [100, 150, 200, 500])
        self.assertEqual(payload["max_training_budget"], 500)
        self.assertEqual(payload["training_mode"], "cumulative_staged_diagnostic_repair")
        self.assertEqual(payload["final_verdict"], "reward_emission_aggregation_repair_ready")

    def test_report_rejects_unsupported_claims_when_ready(self) -> None:
        kwargs = _base_report_kwargs()
        kwargs["final_verdict"] = "reward_emission_aggregation_repair_ready"
        kwargs["claim_safety_status"] = {
            "paper_reproduction_claim_made": True,
            "performance_superiority_claim_made": False,
            "baseline_superiority_claim_made": False,
            "claim_safety_passed": False,
        }
        with self.assertRaises(ValueError):
            RewardEmissionAggregationRepairReport(**kwargs)

    def test_claim_safety_rejects_unsupported_claims(self) -> None:
        with self.assertRaises(ValueError):
            ClaimSafetyStatus(
                paper_reproduction_claim_made=True,
                performance_superiority_claim_made=False,
                baseline_superiority_claim_made=False,
                claim_safety_passed=True,
            )

    def test_diagnostic_decision_rejects_unknown_choice(self) -> None:
        with self.assertRaises(ValueError):
            DiagnosticDecision(recommended_next_action="not_allowed", decision_reason="bad")


